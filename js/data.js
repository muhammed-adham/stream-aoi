// Function to get events based on language
function getEvents(lang = 'en') {
    return eventsData[lang] || eventsData.en;
}

// Function to filter events by date range
function filterEventsByDate(events, startDate, endDate) {
    return events.filter(event => {
        const eventDate = new Date(event.date);
        return eventDate >= startDate && eventDate <= endDate;
    });
}

// Function to search events by title
function searchEventsByTitle(events, searchTerm) {
    const term = searchTerm.toLowerCase();
    return events.filter(event => 
        event.title.toLowerCase().includes(term) ||
        event.description.toLowerCase().includes(term)
    );
}

// Function to get event by ID
function getEventById(id, lang = 'en') {
    const events = getEvents(lang);
    return events.find(event => event.id === id);
} 